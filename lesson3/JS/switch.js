// прогноз погоди
const a = 'ice';
switch (a) {
    case 'sun': {
        console.log('a === take sunglasses');
        break;
    }
    case 'fog':
        console.log('a === do not drive');
        break;
    case 'rain':
        console.log('a === take an umbrella');
        break;
    default:
        console.log('stay at home');
}

